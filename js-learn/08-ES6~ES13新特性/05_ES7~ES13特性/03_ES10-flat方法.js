
/**
 * flat()方法用于将多维数组中的其一层级的所有数组的元素，进行平铺;
 */

// 如下有个三维数组
const nums = [
    [
        [1,2,3]
    ],
    [
        [4,5,6]
    ],
    [
        [7,8,9]
    ]
]

console.log(nums); // [ [ [ 1, 2, 3 ] ], [ [ 4, 5, 6 ] ], [ [ 7, 8, 9 ] ] ]

// 将三维数组，平铺为二维数组
const newNums = nums.flat(1)
console.log(newNums); // [ [ 1, 2, 3 ], [ 4, 5, 6 ], [ 7, 8, 9 ] ]

// 将三维数组，平铺为一维数组
console.log(nums.flat(2)); // [1, 2, 3, 4, 5,6, 7, 8, 9]